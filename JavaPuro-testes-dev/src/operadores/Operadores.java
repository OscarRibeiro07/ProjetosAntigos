package operadores;

public class Operadores {
	public static void main(String[] args) {
		int x = 6;
		System.out.println("x é igual ao numero 6?");
		System.out.println(x ==6);
		
		System.out.println("---------");
		System.out.println("x é diferente do numero 6?");
		System.out.println(x !=6);
		
		System.out.println("---------");
		System.out.println("x é menor ou igual ao numero 6?");
		System.out.println(x <=6);
		
		System.out.println("---------");
		System.out.println("x é maior ou igual ao numero 6?");
		System.out.println(x >=6);
		Integer y = 6;
		// ele compara os objetos
		System.out.println("---------");
		System.out.println("y é um integer? ");
		System.out.println(y instanceof Integer);


	
	
	
	}
}
